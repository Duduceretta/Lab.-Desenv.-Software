    private void preeencheComboPessoas(){
        PessoaDAO pDAO = new PessoaDAO();
        List<Pessoa> listaP = pDAO.getPessoas();
        
        for (Pessoa p : listaP) {
            cmbPessoa.addItem(p);
        }
    }
    
    
